#include <stdio.h>

// 数値演算
int main(void)
{
    printf("%d\n", 100 + 3);
    printf("%d\n", 100 - 3);
    printf("%d\n", 100 * 3);
    printf("%d\n", 100 / 3);
    printf("%d\n", 100 % 3);
    printf("%f\n", 100.0 + 3);
    printf("%f\n", 100.0 - 3);
    printf("%f\n", 100.0 * 3);
    printf("%f\n", 100.0 / 3);
    printf("%f\n", 100 + 3.0);
    printf("%f\n", 100 - 3.0);
    printf("%f\n", 100 * 3.0);
    printf("%f\n", 100 / 3.0);
    printf("%d\n", 100 * 3 / 3);
    printf("%d\n", 100 / 3 * 3);
    printf("%f\n", 100 * 3.0 / 3.0);
    printf("%f\n", 100 / 3.0 * 3.0);
    return 0;
}
